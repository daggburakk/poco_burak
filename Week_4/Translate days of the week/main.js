
let translateDay = prompt("Enter day's name:")

switch(translateDay){
  case "Monday":
   alert("Lundi") 
   break;
  case "Tuesday":
    alert("Mardi")
    break;
  case "Wednesday":
    alert("Mercredi") 
    break;
  case "Thursday":
    alert("Jeudi")
    break;
  case "Friday":
    alert("Vendredi")
    break;
  case "Saturday":
    alert("Samedi")
    break;
  case "Sunday":
    alert("Dimanche")
    default:
        translateDay=alert("Error!!!")          
}